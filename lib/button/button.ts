/**
 * Defines the standard type for button types, which can be one of: 'button', 'submit', 'reset', or undefined.
 */
export type GeneralButtonType = 'button' | 'submit' | 'reset'
export type NuitralButtonType = GeneralButtonType | undefined
/**
 * Defines the position of the icon into the button, which can be one of: 'left' or 'right'
 */
export type NuitralIconPosition = 'left' | 'right'
